const mongoose = require("mongoose");

const installerSchema = new mongoose.Schema({
  installer: {
    type: String,
    required: true,
    validate: /^[A-Za-z0-9 ]*$/
  },
  companyName: {
    type: String,
    required: true,
    validate: /^[A-Za-z0-9 ]*$/
  },
  address: {
    type: String,
    required: true,
    validate: /^[A-Za-z0-9 ]*$/
  },
  city: {
    type: String,
    required: true,
    validate: /^[A-Za-z0-9 ]*$/
  },
  state: {
    type: String,
    required: true,
    validate: /^[A-Za-z0-9 ]*$/
  },
  zipcode: {
    type: String,
    required: true,
    validate: /^[A-Za-z0-9 ]*$/
  },
  phone: {
    type: String,
    required: true,
  },  
  email: {
    type: String,
    required: true,
  }
});

const Installer = mongoose.model("Installer", installerSchema);

module.exports = Installer;